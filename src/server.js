const express = require('express');
const expressGraphQL = require('express-graphql');
const {
    GraphQLSchema,
    GraphQLObjectType,
    GraphQLString,
    GraphQLList,
    GraphQLInt,
    GraphQLNonNull
} = require('graphql');

const cors = require('cors');
const app = express();


const PostType = new GraphQLObjectType({
    name: 'Post',
    description: 'This represents a post written by a user',
    fields: () => ({
        id: {type: GraphQLNonNull(GraphQLInt)},
        userid: {type: GraphQLNonNull(GraphQLInt)},
        text: {type: GraphQLNonNull(GraphQLString)},
        comments: {type: GraphQLNonNull(GraphQLInt)},
        likes: {type: GraphQLNonNull(GraphQLInt)},
        retweets: {type: GraphQLNonNull(GraphQLInt)},
        user: {
            type: UserType,
            resolve: (post) => {
                return users.find(user => user.id === post.userid)
            }
        }
    })
});

const UserType = new GraphQLObjectType({
    name: 'User',
    description: 'An user',
    fields: () => ({
        id: {type: GraphQLNonNull(GraphQLInt)},
        name: {type: GraphQLNonNull(GraphQLString)},
        username: {type: GraphQLNonNull(GraphQLString)},
        profileImg: {type: GraphQLNonNull(GraphQLString)},
        posts: {
            type: GraphQLList(PostType),
            resolve: (user) =>{
                return posts.filter(post => post.id === user.id)
            }
        }
    })
});



const RootQueryType = new GraphQLObjectType({
    name: 'Query',
    description: 'Root Query',
    fields: () => ({
        post: {
            type: PostType,
            description: 'A single post',
            args: {
                id: {type: GraphQLInt}
            },
            resolve: (parent, args) => posts.find(post => post.id === post.id)
        },
        posts: {
            type: new GraphQLList(PostType),
            description: 'List of posts',
            resolve: () => posts
        },
        users: {
            type: new GraphQLList(UserType),
            description: 'List of all users',
            resolve: () => users
        },
        user: {
            type: UserType,
            description: 'A single user',
            args: {
                id: {type: GraphQLInt}
            },
            resolve: (parent, args) => users.find(user => user.id === args.id)
        },
    })
});
